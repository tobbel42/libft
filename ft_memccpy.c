#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	size_t		i;
	char		*ptr_dest;
	char		*ptr_src;

	ptr_dest = (char *)dest;
	ptr_src = (char *)src;
	i = 0;
	while (i < n)
	{
		ptr_dest[i] = ptr_src[i];
		if ((unsigned char)ptr_src[i] == (unsigned char)c)
			return (dest + i + 1);
		i++;
	}
	return (0);
}
